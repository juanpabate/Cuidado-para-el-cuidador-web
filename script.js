//MODAL 1
const modalButon1 = document.getElementById('tecnologia1');
const modal1= document.getElementById('modal1');
const modalClose1= document.getElementById('modalClose1');

modalButon1.addEventListener('click', ()=>{
    modal1.classList.add('showModal');
});
modalClose1.addEventListener('click', ()=>{
    modal1.classList.remove('showModal');
})


